# Numbers

num1 = 24
num2 = 87

# Addition
Add = num1 + num2
print(Add)

# Subtraction
Sub = num2 - num1
print(Sub)

# Multiplication
Mult = num1 * num2
print(Mult)

# Division
Div = num2 / num1
print(Div)

# Reminder
Rem = num2 % num1
print(Rem)

# Power
Pow = 2 ** 2
print(Pow)

# Round Function
Rou = 1.26895959895
Round = round(Rou, 2)
print(Round)

